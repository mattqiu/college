echo floor(1.99) . ' ' . ceil(1.01) . '<br>';

#分页功能：X条，每页Y条，页数Z
$x = 102;
$y = 10;
echo ceil($x/$y) . '<br>';

#-------------------------------

#幂运算
echo pow(5, 2), '<br>'; 	#5*5

#平方根
echo sqrt(24), '<br>';

echo sqrt(-1), '<br>';		#NAN

#-------------------------------

echo max(1, 2, 5, 3, 3) . ' ' . min(1, 2, 5, 3, 3) . '<br>';

#-------------------------------

echo rand(1, 100), '<br>';

#更好的随机数
echo mt_rand(1, 100), '<br>';

#-------------------------------

#四舍五入

